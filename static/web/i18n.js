/** Return translated string for *key*, falling back to English. */
function t(key) {
  return (I18N[browserLang] && I18N[browserLang][key]) || I18N.en[key] || key;
}

/** Return translated string for *key* in a specific *lang*. */
function tLang(key, lang) {
  return (I18N[lang] && I18N[lang][key]) || I18N.en[key] || key;
}

/** Replace text in all elements with [data-i18n] attributes. */
function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const translated = t(key);
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.placeholder = translated;
    } else {
      el.textContent = translated;
    }
  });
  document.documentElement.lang = browserLang;
}

// Auto-apply translations on DOMContentLoaded (runs once per page)
document.addEventListener("DOMContentLoaded", applyTranslations);
